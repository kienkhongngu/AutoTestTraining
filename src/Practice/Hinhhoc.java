package Practice;

public abstract class Hinhhoc {
	private String hinhtron;
	private String hinhchunhat;
	public Hinhhoc() {
		
	}
	public Hinhhoc(String hinhtron, String hinhchunhat) {
		super();
		this.hinhtron = hinhtron;
		this.hinhchunhat = hinhchunhat;
	}
	public String getHinhtron() {
		return hinhtron;
	}
	public void setHinhtron(String hinhtron) {
		this.hinhtron = hinhtron;
	}
	public String getHinhchunhat() {
		return hinhchunhat;
	}
	public void setHinhchunhat(String hinhchunhat) {
		this.hinhchunhat = hinhchunhat;
	}
	
	
	
}
